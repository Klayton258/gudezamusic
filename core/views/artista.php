                    <P>Iniciou a sua carreira musical aos 14 anos de idade, enquanto ainda estava frequentar o ensino secundário. Lançou a sua primeira música oficial do “<strong>Curtir A Vibe</strong>” música esta que abriu-lhe várias portas na indústria da música e lhe fez ganhar uma certa popularidade no mercado nacional. Alguns anos depois, ele fundou sua própria gravadora independente “<strong>Gudeza Music</strong>” com o rapper Alsina Star, onde lançou três EP’s em anos seguidas na mesma: “<strong>Sentimentos Não Tem Idade</strong>” em 2017, “<strong>Quem Ama Sofre</strong>” em 2018 e “<strong>O Encantador Delas</strong>” em 2019.
                    </P>
